package com.github.beelzebu.coins.api.utils;

import java.util.Map;

/**
 * @author Beelzebu
 */
public class CoinsEntry <K, V> implements Map.Entry<K, V> {

    private K key;
    private V value;

    public CoinsEntry(K key, V value) {
        this.key = key;
        this.value = value;
    }

    @Override
    public K getKey() {
        return key;
    }

    @Override
    public V getValue() {
        return value;
    }

    @Override
    public V setValue(V v) {
        return value = v;
    }

    public K setKey(K k) {
        return key = k;
    }
}
